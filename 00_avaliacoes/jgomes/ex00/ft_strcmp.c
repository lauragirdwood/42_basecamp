int		ft_strcmp(char *s1, char *s2)
{
	int a;
	int rest;

	a = 0;
	while ((s1[a]) != '\0')
	{
		rest = (s1[a]) - (s2[a]);
		if (rest != 0)
			return (rest);
		a++;
	}
	if ((s2[a]) != '\0')
		return ((s1[a]) - (s2[a]));
	return (0);
}

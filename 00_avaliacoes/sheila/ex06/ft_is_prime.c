int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	while (nb % i != 0)
		i++;
	if (nb == i)
		return (1);
	return (0);
}
